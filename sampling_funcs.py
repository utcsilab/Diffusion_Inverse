def similarity_loss(x,temp):
    batch_sz = x.shape[0]
    loss = 0
    for i in batch_sz:
        for j in range(i,batch_sz):
            loss = loss + torch.exp((-1*torch.norm(x[i]-x[j])**2)/temp)
    return loss

